public class Exce2 {

	public static void main(String[] args) {
		int i, j, size=10;
		for(i=1;i<=size;i++){
			for(j=1;j<=size;j++){
				if(i==1 ||j==1|| i==size||j==size)	
						
				System.out.print(" * ");
				else{
					System.out.print("    ");
				}
				}
		
			System.out.println("");
			
		}
	}

}
